package com.example.wannacry.gridrecycler;

public class Jomblo {
    private String nama;
    private String story;

    public Jomblo(String nama, String story) {

        this.nama = nama;
        this.story = story;
    }

    public String getNama() {
        return nama;
    }

    public String getStory() {
        return story;
    }
}
